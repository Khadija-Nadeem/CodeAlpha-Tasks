    private void loadAvailableRooms() {
    // SQL query to get available rooms and their types
    String sql = "SELECT room_number, room_type FROM rooms WHERE availability_status = 'Available'";
    
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Hotel_Reservation_System", "root", "programmer._.2004");
         PreparedStatement pstmt = con.prepareStatement(sql);
         ResultSet rs = pstmt.executeQuery()) {
        
        // Create a model for the combo box
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
     
        while (rs.next()) {
            // Fetch room number and type
            String roomNumber = rs.getString("room_number");
            String roomType = rs.getString("room_type");
            
            // Add formatted room info to the model
            model.addElement(roomNumber + " (" + roomType + ")");
        }
        
        cbAvailableRooms.setModel(model);     
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading room data.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    public void updateRoomDetailsFromSelection() {
    String selectedRoom = (String) cbAvailableRooms.getSelectedItem();
    
    if (selectedRoom != null) {
        // Split the selected item into room number and type
        String[] parts = selectedRoom.split(" ");
        selectedRoomNumber = Integer.parseInt(parts[0]); // Parse room number
        selectedRoomType = parts[1].replace("(", "").replace(")", ""); // Extract room type
        
        // Set the room number and type to text fields
        txtRoomNumber.setText(String.valueOf(selectedRoomNumber));
        txtRoomType.setText(selectedRoomType);
    }
}


