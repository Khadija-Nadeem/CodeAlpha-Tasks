    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Get the text from the room number and room type fields
    String roomNumberText = txtRoomNumber.getText().trim();
    String roomTypeText = txtRoomType.getText().trim();

    // Check if either field is empty
    if (roomNumberText.isEmpty() || roomTypeText.isEmpty()) {
        // Show an error message if either field is empty
        JOptionPane.showMessageDialog(this, "Please select a room number and room type from drop down.", "Validation Error", JOptionPane.ERROR_MESSAGE);
    } else {
         Reservations room = new Reservations("Summer Sale Extravaganza", selectedRoomNumber, selectedRoomType);
    room.setVisible(true);
    dispose();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

